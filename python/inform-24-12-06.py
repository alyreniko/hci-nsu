# === Задача 3 ===
# Дано натуральное число N и последовательность a1,a2,…, aNцелых чисел.
# Элементы последовательности – случайные числа из диапазона от 1 до 125
# включительно. Найти минимальный по модулю элемент и создать новый массив из
# элементов массива a, кратных найденному элементу. Задачу решить для N= 20.
# Нахождение минимума оформить в виде функции.
def prog3():
    def find_min(a):
        min_val = abs(a[0])
        min_index = 0
        for i in range(len(a)):
            if abs(a[i]) < min_val:
                min_val = abs(a[i])
                min_index = i
        return min_val, min_index

    N = 20
    a = [random.randint(1, 125) for _ in range(N)]
    print("Последовательность:", a)

    min_val, min_index = find_min(a)
    print(f"Минимальный по модулю элемент: a[{min_index}] = {min_val}")

    new_a = [x for x in a if x % min_val == 0]
    print("Новый массив из элементов, кратных минимальному по модулю:", new_a)

# === Задача 4 ===
# Дан массив целых чисел(N=10), заполненный случайным образом числами из
# промежутка от -10 до 10 включительно. Удалить из него все числа от 0 до 5.
def prog4_1():
    a = [random.randint(-10, 10) for _ in range(10)]
    print("Исходный массив:", a)

    a = [x for x in a if x < 0 or x > 5]
    print("Новый массив:", a)

def prog4_2():
    a = [random.randint(-10, 10) for _ in range(10)]
    print("Исходный массив:", a)

    for i in range(0, 5):
        if i in a:
            a.remove(i)
    print("Новый массив:", a)

