import random

# === Задача 1 ===
# Дано натуральное число N и последовательность a1,a2,…, aNцелых чисел,
# заданных случайным образом из диапазона от -15 до 35 включительно. Найти
# сумму положительных элементов, стоящих на нечетных местах массива, если
# сумма индексов максимального и минимального элементов больше 5, в противном
# случае – посчитать количество отрицательных элементов массива. Задачу решить
# для N=10.
def prog1():
    N = 10
    a = [random.randint(-15, 35) for _ in range(N)]
    print(f"Последовательность: {a}")

    if a.index(min(a)) + a.index(max(a)) > 5:
        sum_positive = 0
        for i in range(N):
            if i % 2 != 0 and a[i] > 0:
                sum_positive += a[i]
        print("Сумма положительных элементов, стоящих на нечетных местах:", sum_positive)
    else:
        negative_count = 0
        for i in range(N):
            if a[i] < 0:
                negative_count += 1
        print("Количество отрицательных элементов:", negative_count)

